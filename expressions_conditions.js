let a = 45;
let b = 5;

console.log("a+ b = ", a + b);
console.log("a - b = ", a - b);
console.log("a * b = ", a * b);
console.log("a / b = ", a / b);
console.log("a ** b = ", a ** b);
console.log("a % b = ", a % b);

console.log("a++ = ", a++);
console.log("after a++ = ", a);

console.log("a-- = ", a--);
console.log("after a-- = ", a);

console.log("++a = ", ++a);
console.log("after  ++a = ", a);

console.log("--a = ", --a);
console.log("after --a = ", a);

// Assignment Operator
a += 5;
a -= 5;
a *= 5;
a /= 5;
a %= 5;
a **= 5;

a == b; // equal to
a != b; // not equal
a === b; // equal data and type
a !== b; // not equal data or type
a > b; // greater than data
a < b; // lesser than data
a >= b; // greater or equal data
a <= b; // lesser or equal data

a == b ? console.log("OK") : console.log("NO");

